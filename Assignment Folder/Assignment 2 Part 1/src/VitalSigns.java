public class VitalSigns {
    private int respiratoryRate;
    private int heartRate;
    private int bloodPressure;
    private double weightInKilo;
    private double weightInPound;

    public int getRespiratoryRate() {
        return respiratoryRate;
    }

    public void setRespiratoryRate(int respiratoryRate) {
        this.respiratoryRate = respiratoryRate;
    }

    public int getHeartRate() {
        return heartRate;
    }

    public void setHeartRate(int heartRate) {
        this.heartRate = heartRate;
    }

    public int getBloodPressure() {
        return bloodPressure;
    }

    public void setBloodPressure(int bloodPressure) {
        this.bloodPressure = bloodPressure;
    }

    public double getWeightInKilo() {
        return weightInKilo;
    }

    public void setWeightInKilo(double weightInKilo) {
        this.weightInKilo = weightInKilo;
    }

    public double getWeightInPound() {
        return weightInPound;
    }

    public void setWeightInPound(double weightInPound) {
        this.weightInPound = weightInPound;
    }
       
    
}
